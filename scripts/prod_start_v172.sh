#!/bin/bash
# ============================================================================
# V172 数据收割引擎 - 生产启动脚本
# ============================================================================
#
# 版本: V172.1.0 (Stable)
# 日期: 2026-02-27
#
# 使用方法:
#   ./scripts/prod_start_v172.sh                    # 标准模式
#   ./scripts/prod_start_v172.sh --repair           # 修复模式 (重收割坏账)
#   ./scripts/prod_start_v172.sh --workers 3        # 指定 Worker 数量
#   ./scripts/prod_start_v172.sh --delay 8000 15000 # 自定义延时范围
#
# ============================================================================

set -e

# ============================================================================
# 颜色定义
# ============================================================================

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# ============================================================================
# 默认配置
# ============================================================================

WORKERS=5
MIN_DELAY=5000
MAX_DELAY=12000
MODE="standard"
DRY_RUN=false

# ============================================================================
# 参数解析
# ============================================================================

while [[ $# -gt 0 ]]; do
    case $1 in
        --workers|-w)
            WORKERS="$2"
            shift 2
            ;;
        --delay|-d)
            MIN_DELAY="$2"
            MAX_DELAY="$3"
            shift 3
            ;;
        --repair|-r)
            MODE="repair"
            shift
            ;;
        --dry-run)
            DRY_RUN=true
            shift
            ;;
        --help|-h)
            echo "V172 数据收割引擎 - 生产启动脚本"
            echo ""
            echo "使用方法:"
            echo "  $0 [选项]"
            echo ""
            echo "选项:"
            echo "  --workers, -w N      设置 Worker 数量 (默认: 5)"
            echo "  --delay, -d MIN MAX  设置延时范围 (默认: 5000 12000)"
            echo "  --repair, -r         启动修复模式"
            echo "  --dry-run            仅显示配置，不执行"
            echo "  --help, -h           显示帮助信息"
            exit 0
            ;;
        *)
            echo -e "${RED}未知参数: $1${NC}"
            exit 1
            ;;
    esac
done

# ============================================================================
# 环境检查
# ============================================================================

echo -e "${BLUE}"
echo "═══════════════════════════════════════════════════════════════════════════════"
echo "  V172 数据收割引擎 - 生产启动器"
echo "═══════════════════════════════════════════════════════════════════════════════"
echo -e "${NC}"

# 检查 Docker 环境
if ! command -v docker &> /dev/null; then
    echo -e "${RED}❌ Docker 未安装${NC}"
    exit 1
fi

# 检查开发容器是否运行
if ! docker-compose ps dev 2>/dev/null | grep -q "Up"; then
    echo -e "${YELLOW}⚠️  开发容器未运行，正在启动...${NC}"
    docker-compose -f docker-compose.dev.yml up -d
    sleep 5
fi

# 检查数据库连接
echo -e "${BLUE}🔍 检查数据库连接...${NC}"
if ! docker-compose exec -T db pg_isready -U football_user -d football_db &> /dev/null; then
    echo -e "${RED}❌ 数据库连接失败${NC}"
    exit 1
fi
echo -e "${GREEN}✅ 数据库连接正常${NC}"

# ============================================================================
# 显示配置
# ============================================================================

echo ""
echo -e "${BLUE}📋 运行配置:${NC}"
echo -e "   模式:       ${YELLOW}${MODE}${NC}"
echo -e "   Worker 数量: ${YELLOW}${WORKERS}${NC}"
echo -e "   延时范围:   ${YELLOW}${MIN_DELAY}ms ~ ${MAX_DELAY}ms${NC}"
echo ""

if [ "$DRY_RUN" = true ]; then
    echo -e "${YELLOW}🔍 Dry-run 模式，不执行收割${NC}"
    exit 0
fi

# ============================================================================
# 执行收割
# ============================================================================

echo -e "${GREEN}🚀 启动 V172 装甲群收割器...${NC}"
echo ""

# 构建环境变量
ENV_VARS="MAX_WORKERS=${WORKERS} MIN_DELAY_MS=${MIN_DELAY} MAX_DELAY_MS=${MAX_DELAY}"

# 执行收割命令
docker-compose exec -e MAX_WORKERS=${WORKERS} \
    -e MIN_DELAY_MS=${MIN_DELAY} \
    -e MAX_DELAY_MS=${MAX_DELAY} \
    dev node scripts/ops/harvest_fleet_master.js

# ============================================================================
# 完成
# ============================================================================

echo ""
echo -e "${GREEN}═══════════════════════════════════════════════════════════════════════════════${NC}"
echo -e "${GREEN}  ✅ V172 收割任务完成${NC}"
echo -e "${GREEN}═══════════════════════════════════════════════════════════════════════════════${NC}"
